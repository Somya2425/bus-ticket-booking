package com.aditi.transitflow.enums;

/**
 * Enum representing the availability status of a resource,
 * such as a seat in a bus booking system.
 * This enum is commonly used to indicate whether a seat
 * is available for booking or already booked.
 */
public enum Status {

    /**
     * Indicates that the resource (e.g., seat) is available for booking.
     */
    Available,

    /**
     * Indicates that the resource (e.g., seat) has already been booked.
     */
    Booked
}