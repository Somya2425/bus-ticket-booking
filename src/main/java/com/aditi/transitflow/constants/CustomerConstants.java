package com.aditi.transitflow.constants;

/**
 * This class contains constant values related to Customer operations.
 * It is used to store reusable messages such as:
 * - Customer not found message
 * - Booking not found message
 * These constants help in maintaining consistency across the application.
 */
public class CustomerConstants {

    /**
     * Message displayed when a customer is not found.
     */
    public static final String CUSTOMER_NOT_FOUND = "Customer not found";

    /**
     * Message displayed when no bookings are found for a customer.
     */
    public static final String BOOKINGS_NOT_FOUND = "No bookings found for this customer";
}
